#ifndef WARBLER_PRIMITIVE_HPP
#define WARBLER_PRIMITIVE_HPP

// standard headers
#include <cstdint>

namespace warbler
{
	// unsigned integers

	typedef uint8_t		byte;
	typedef uint8_t		u8;
	typedef uint16_t	u16;
	typedef uint32_t	u32;
	typedef uint64_t	u64;

	// signed integers

	typedef int8_t		i8;
	typedef int16_t		i16;
	typedef int32_t		i32;
	typedef int64_t		i64;

	// floating point

	typedef float		f32;
	typedef double		f64;
}
#endif
